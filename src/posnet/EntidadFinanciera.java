package posnet;

/**
 * La clase EntidadFinanciera es una enumeración que contiene los valores de 
 * las dos entidades financieras que se utilizan en el sistema Posnet.
 * 
 * Cada valor de la enumeración representa una entidad financiera, BIRZA y 
 * CASTERMARD, que pueden ser utilizadas para efectuar pagos a través
 * del sistema Posnet.
 * 
 * Esta clase no tiene métodos.
*/
public enum EntidadFinanciera {
    BIRZA,CASTERMARD;
}
